    public void fillToTable() {
        try {
            DefaultTableModel model = (DefaultTableModel) tblGridView.getModel();
            model.setRowCount(0);
            String name = txtTimKiem.getText();
            List<NguoiHoc> list = dao.selectByName(name);
            list.forEach((row) -> {
                model.addRow(new Object[]{row.getMaNH(), row.getHoTen(), row.getNgaySinh(), row.getGioiTinh() == 1 ? "Nam" : "Nữ", row.getDienThoai(),
                    row.getEmail(), row.getMaNV(), row.getNgayDK()});
            });
        } catch (Exception e) {
        }
    }

    public void showDetail(NguoiHoc nh) {
        txtMaNH.setText(nh.getMaNH());
        txtHoTen.setText(nh.getHoTen());
        txtNgaySinh.setText(nh.getNgaySinh() + "");
        cboGioiTinh.setSelectedIndex(nh.getGioiTinh() == 1 ? 0 : 1);
        txtDienThoai.setText(nh.getDienThoai());
        txtEmail.setText(nh.getEmail());
        txtGhiChu.setText(nh.getGhiChu());
    }

    public void setStatus(boolean insertable) {
        btnInsert.setEnabled(insertable);
        btnUpdate.setEnabled(!insertable);
        btnDelete.setEnabled(!insertable);
        btnNext.setEnabled(!insertable);
        btnPrev.setEnabled(!insertable);
        btnLast.setEnabled(!insertable);
        btnFirst.setEnabled(!insertable);
        txtMaNH.setEditable(insertable);
    }

    public void edit() {
        NguoiHoc nh = dao.selectByID(tblGridView.getValueAt(index, 0) + "");
        showDetail(nh);
        setStatus(false);
    }

    public boolean validateForm() {
        if (txtMaNH.getText().length() == 0 || txtHoTen.getText().length() == 0 || txtNgaySinh.getText().length() == 0
                || txtDienThoai.getText().length() == 0 || txtEmail.getText().length() == 0 || txtGhiChu.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Hãy điền đủ thông tin");
            return false;
        }
        if (getModel() == null) {
            JOptionPane.showMessageDialog(this, "Ngày sinh không hợp lệ");
            return false;
        }
        return true;
    }

    public NguoiHoc getModel() {
        try {
            return new NguoiHoc(txtMaNH.getText(), txtHoTen.getText(), XDate.toDate(txtNgaySinh.getText(), "dd-MM-yyyy"), cboGioiTinh.getSelectedIndex(),
                    txtDienThoai.getText(), txtEmail.getText(), txtGhiChu.getText(), Auth.user.getMaNV(), new Date());
        } catch (Exception e) {
            return null;
        }
    }

    public void insert() {
        if (validateForm()) {
            if (dao.selectByID(txtMaNH.getText()) != null) {
                JOptionPane.showMessageDialog(this, "Mã nhân viên đã tồn tại");
            }
            dao.insert(getModel());
            fillToTable();
            JOptionPane.showMessageDialog(this, "Thêm mới thành công");
        }
    }

    public void update() {
        validateForm();
        if (validateForm()) {
            dao.update(getModel());
            fillToTable();
            JOptionPane.showMessageDialog(this, "Cập nhật  thành công");
        }
    }

    public void delete() {
        try {
            dao.delete(txtMaNH.getText());
            fillToTable();
            JOptionPane.showMessageDialog(this, "Xóa thành công");
            clear();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Không thể xóa người học này");
        }
    }

    public void clear() {
        setStatus(true);
        txtMaNH.setText("");
        txtHoTen.setText("");
        txtNgaySinh.setText("");
        txtDienThoai.setText("");
        txtEmail.setText("");
        txtGhiChu.setText("");
        cboGioiTinh.setSelectedIndex(0);
    }
